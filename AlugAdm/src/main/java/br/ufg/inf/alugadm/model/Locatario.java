package br.ufg.inf.alugadm.model;

/**
 *
 * @author JuliannyAS
 */
public class Locatario {

    private String nome;
    private String cpf;
    private String rg;
    private String orgao;
    private String sexo;
    private String dataNascimento;
    private String telefone1;
    private String telefone2;
    private String codigoLocatario;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    public String getRg() {
        return rg;
    }

    public void setRg(String rg) {
        this.rg = rg;
    }

    public String getOrgao() {
        return orgao;
    }

    public void setOrgao(String orgao) {
        this.orgao = orgao;
    }

    public String getSexo() {
        return sexo;
    }

    public void setSexo(String sexo) {
        this.sexo = sexo;
    }

    public String getDataNascimento() {
        return dataNascimento;
    }

    public void setDataNascimento(String dataNascimento) {
        this.dataNascimento = dataNascimento;
    }

    public String getTelefone1() {
        return telefone1;
    }

    public void setTelefone1(String telefone1) {
        this.telefone1 = telefone1;
    }

    public String getTelefone2() {
        return telefone2;
    }

    public void setTelefone2(String telefone2) {
        this.telefone2 = telefone2;
    }

    public String getCodigoLocatario() {
        return codigoLocatario;
    }

    public void setCodigoLocatario(String codigoLocatario) {
        this.codigoLocatario = codigoLocatario;
    }

}
